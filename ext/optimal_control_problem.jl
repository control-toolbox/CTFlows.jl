"""
$(TYPEDSIGNATURES)

Construct a flow for a control-free optimal control problem.

This method builds the Hamiltonian system for problems without control variables
(parameter estimation, optimal design). The control is internally set to an empty array.

# Arguments

- `ocp::ControlFreeModel`: A control-free optimal control problem (with `EmptyControlModel`).
- `alg`: Integration algorithm (default inferred).
- `abstol`: Absolute tolerance for the ODE solver.
- `reltol`: Relative tolerance for the ODE solver.
- `saveat`: Time points at which to save the solution.
- `internalnorm`: Optional norm function used by the integrator.
- `kwargs_Flow`: Additional keyword arguments passed to the solver.

# Returns

A flow object `f` such that:

- `f(t0, x0, p0, tf)` integrates the state and costate from `t0` to `tf`.
- `f((t0, tf), x0, p0)` returns the full trajectory over the interval.

# Example

```julia-repl
julia> # Control-free problem (parameter estimation)
julia> ocp = @def begin
           ╬╗ Ōłł R, variable
           t Ōłł [0, 10], time
           x Ōłł R, state
           x(0) == 2.0
           ß║ŗ(t) == ╬╗ * x(t)
           Ōł½((x(t) - data(t))^2) ŌåÆ min
       end

julia> f = Flow(ocp)
julia> sol = f((0.0, 10.0), x0, p0, ╬╗)
```

# Notes

This method is for control-free problems only. For problems with control variables,
use `Flow(ocp, u)` instead.
"""
function CTFlows.Flow(
    ocp::ControlFreeModel;
    alg=__alg(),
    abstol=__abstol(),
    reltol=__reltol(),
    saveat=__saveat(),
    internalnorm=__internalnorm(),
    kwargs_Flow...,
)
    h, u_dummy = __create_hamiltonian(ocp)
    return __ocp_Flow(
        ocp, h, u_dummy, alg, abstol, reltol, saveat, internalnorm; kwargs_Flow...
    )
end

"""
$(TYPEDSIGNATURES)

Construct a flow for an optimal control problem using a given control law.

This method builds the Hamiltonian system associated with the optimal control problem (`ocp`)
and integrates the corresponding stateŌĆōcostate dynamics using the specified control law `u`.

# Arguments

- `ocp::WithControlModel`: An optimal control problem with control variables.
- `u::CTFlows.ControlLaw`: A feedback control law generated by `ControlLaw(...)` or similar.
- `alg`: Integration algorithm (default inferred).
- `abstol`: Absolute tolerance for the ODE solver.
- `reltol`: Relative tolerance for the ODE solver.
- `saveat`: Time points at which to save the solution.
- `internalnorm`: Optional norm function used by the integrator.
